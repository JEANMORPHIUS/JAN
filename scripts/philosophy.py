# PHILOSOPHY CHECK FUNCTION
def check_alignment(purpose: str, serves_mission: bool = True, serves_love: bool = True, 
                   serves_truth: bool = True, serves_community: bool = True,
                   is_authentic: bool = True, is_simple: bool = True, is_moderate: bool = True) -> bool:
    """
    Check if code aligns with philosophy.
    
    Args:
        purpose: What this code does
        serves_mission: Does it serve stewardship and community?
        serves_love: Does it serve love (highest mastery)?
        serves_truth: Does it serve truth (knowledge over belief)?
        serves_community: Does it serve community (We All Win)?
        is_authentic: Is it authentic and aligned? (Non-negotiable)
        is_simple: Is it simple? Life is simple - don't complicate it.
        is_moderate: Is it moderate? Everything in moderation.
        
    Returns:
        True if aligned, False otherwise
    """
    alignment = {
        "mission": serves_mission,
        "love": serves_love,
        "truth": serves_truth,
        "community": serves_community,
        "authentic": is_authentic,  # Non-negotiable
        "simple": is_simple,  # Life is simple
        "moderate": is_moderate  # Everything in moderation
    }
    
    # Authentic and aligned is non-negotiable
    if not is_authentic:
        print(f"ALIGNMENT CHECK FAILED - NON-NEGOTIABLE: {purpose}")
        print("Authentic and aligned is non-negotiable. Cannot proceed.")
        return False
    
    if not all(alignment.values()):
        print(f"Alignment check failed for: {purpose}")
        print(f"Missing alignment: {[k for k, v in alignment.items() if not v]}")
        return False
    
    return True
